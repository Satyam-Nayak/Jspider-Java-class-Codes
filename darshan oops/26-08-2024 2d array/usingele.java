public class usingele {
        public static void main(String[] args) {
            int[][] arr = {{1, 2, 3,7,9},{4,5},{7, 8, 9}};
            for(int[] ele:arr){
                for(int item:ele){
                    System.out.print(item+" ");
            }
            System.out.println();
            }
        }
    }
    
    

