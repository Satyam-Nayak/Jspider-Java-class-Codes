// write a java program to print the string in reverse order.
public class q2 {
    public static void main(String[] args) {
        // Reverse the string
        String s = "Hello Satyam";
        for(int i = s.length()-1; i >= 0; i--) {
            System.out.println(s.charAt(i));
        }
    
    }
    
}
